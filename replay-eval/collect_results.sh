#!/usr/bin/env bash
# collect_results.sh - Gather TestBot output after it completes
#
# Usage: ./collect_results.sh <PR_NUMBER>
#
# Collects:
#   - TestBot-generated test files from the PR branch
#   - TestBot's PR comment (analysis/recommendations)
#   - Workflow run logs
#
# Prerequisites:
#   - trigger_testbot.sh has been run
#   - TestBot has completed its run

set -euo pipefail

FORK_REPO="sxarsky/prefect-testbot-eval"

PR_NUMBER="${1:?Usage: $0 <PR_NUMBER>}"
PATCHES_DIR="./patches/pr${PR_NUMBER}"
RESULTS_DIR="./results/replay/pr${PR_NUMBER}"

echo "=== Collecting TestBot results for PR #${PR_NUMBER} ==="

mkdir -p "${RESULTS_DIR}/generated"
mkdir -p "${RESULTS_DIR}/comments"
mkdir -p "${RESULTS_DIR}/logs"

# Get the fork PR number
FORK_PR_NUMBER=$(cat "${PATCHES_DIR}/fork_pr_number.txt" 2>/dev/null || echo "")
if [ -z "${FORK_PR_NUMBER}" ]; then
    echo "ERROR: Fork PR number not found. Was trigger_testbot.sh run?"
    exit 1
fi

BRANCH_NAME="replay/pr${PR_NUMBER}"

echo "Fork PR: #${FORK_PR_NUMBER}"
echo "Branch: ${BRANCH_NAME}"

# Step 1: Fetch the latest state of the branch (including TestBot commits)
echo ""
echo "--- Step 1: Fetching latest branch state ---"
git fetch origin "${BRANCH_NAME}"

# Step 2: Identify TestBot's commits
echo ""
echo "--- Step 2: Identifying TestBot commits ---"

# Get the replay commit (our code-only commit)
REPLAY_COMMIT=$(git log --oneline "origin/${BRANCH_NAME}" --grep="Replay PR" --format="%H" | head -1)

# Everything after our replay commit is from TestBot
if [ -n "${REPLAY_COMMIT}" ]; then
    TESTBOT_COMMITS=$(git log --oneline "${REPLAY_COMMIT}..origin/${BRANCH_NAME}" --format="%H %s")
    if [ -n "${TESTBOT_COMMITS}" ]; then
        echo "TestBot commits found:"
        echo "${TESTBOT_COMMITS}" | while read -r hash msg; do
            echo "  $(echo $hash | cut -c1-8) ${msg}"
        done
    else
        echo "WARNING: No TestBot commits found after replay commit."
        echo "TestBot may not have run or may have failed silently."
    fi
else
    echo "WARNING: Could not identify replay commit. Checking all commits..."
    TESTBOT_COMMITS=$(git log --oneline "origin/${BRANCH_NAME}" --grep -iE "testbot|skyramp" --format="%H %s")
fi

# Step 3: Extract generated test files
echo ""
echo "--- Step 3: Extracting generated test files ---"

# Get the diff between our replay commit and the branch tip
# This diff contains everything TestBot added
if [ -n "${REPLAY_COMMIT}" ]; then
    GENERATED_FILES=$(git diff --name-only "${REPLAY_COMMIT}" "origin/${BRANCH_NAME}" 2>/dev/null || echo "")

    if [ -n "${GENERATED_FILES}" ]; then
        echo "Generated/modified files:"
        while IFS= read -r file; do
            [ -z "$file" ] && continue
            echo "  ${file}"

            # Extract file content
            mkdir -p "${RESULTS_DIR}/generated/$(dirname "$file")"
            git show "origin/${BRANCH_NAME}:${file}" > "${RESULTS_DIR}/generated/${file}" 2>/dev/null || {
                echo "    WARNING: Could not extract ${file}"
            }
        done <<< "${GENERATED_FILES}"
    else
        echo "No files were generated by TestBot."
    fi

    # Save the diff itself
    git diff "${REPLAY_COMMIT}" "origin/${BRANCH_NAME}" > "${RESULTS_DIR}/testbot-diff.patch" 2>/dev/null || true
fi

# Step 4: Collect PR comments (TestBot's analysis)
echo ""
echo "--- Step 4: Collecting PR comments ---"
COMMENTS=$(gh api "repos/${FORK_REPO}/issues/${FORK_PR_NUMBER}/comments" \
    --jq '.[] | {id: .id, author: .user.login, created: .created_at, body: .body}' 2>/dev/null || echo "")

if [ -n "${COMMENTS}" ]; then
    # Save all comments
    gh api "repos/${FORK_REPO}/issues/${FORK_PR_NUMBER}/comments" > "${RESULTS_DIR}/comments/all_comments.json" 2>/dev/null

    # Extract TestBot-specific comments
    gh api "repos/${FORK_REPO}/issues/${FORK_PR_NUMBER}/comments" \
        --jq '.[] | select(.user.login | test("(?i)testbot|skyramp|bot")) | .body' \
        > "${RESULTS_DIR}/comments/testbot_comments.md" 2>/dev/null || true

    COMMENT_COUNT=$(gh api "repos/${FORK_REPO}/issues/${FORK_PR_NUMBER}/comments" --jq 'length' 2>/dev/null || echo "0")
    echo "Collected ${COMMENT_COUNT} comments"
else
    echo "No comments found on PR."
fi

# Step 5: Collect workflow run logs
echo ""
echo "--- Step 5: Collecting workflow logs ---"
RUNS=$(gh run list --repo "${FORK_REPO}" --branch "${BRANCH_NAME}" \
    --json databaseId,name,status,conclusion,startedAt,updatedAt --limit 5 2>/dev/null || echo "[]")

echo "${RUNS}" | jq '.' > "${RESULTS_DIR}/logs/workflow_runs.json" 2>/dev/null || true

# Get the most recent run's logs
LATEST_RUN_ID=$(echo "${RUNS}" | jq -r '.[0].databaseId' 2>/dev/null || echo "")
if [ -n "${LATEST_RUN_ID}" ] && [ "${LATEST_RUN_ID}" != "null" ]; then
    echo "Latest workflow run: ${LATEST_RUN_ID}"
    gh run view "${LATEST_RUN_ID}" --repo "${FORK_REPO}" --log > "${RESULTS_DIR}/logs/run_${LATEST_RUN_ID}.log" 2>/dev/null || {
        echo "  Could not download full logs (run may still be in progress)"
    }
fi

# Step 6: Generate summary
echo ""
echo "--- Step 6: Generating summary ---"

GENERATED_TEST_COUNT=$(find "${RESULTS_DIR}/generated" -name "*.py" -path "*/test*" 2>/dev/null | wc -l | tr -d ' ')
GROUND_TRUTH_TEST_COUNT=$(find "${PATCHES_DIR}/ground-truth" -name "*.py" -path "*/test*" 2>/dev/null | wc -l | tr -d ' ')

cat > "${RESULTS_DIR}/collection_summary.md" <<SUMEOF
# Collection Summary: PR #${PR_NUMBER}

**Collected:** $(date -u +%Y-%m-%dT%H:%M:%SZ)
**Fork PR:** #${FORK_PR_NUMBER}
**Branch:** ${BRANCH_NAME}

## Files Collected

| Category | Count |
|----------|-------|
| Generated test files | ${GENERATED_TEST_COUNT} |
| Ground truth test files | ${GROUND_TRUTH_TEST_COUNT} |
| PR comments | ${COMMENT_COUNT:-0} |
| Workflow runs | $(echo "${RUNS}" | jq 'length' 2>/dev/null || echo "0") |

## Generated Files
$(find "${RESULTS_DIR}/generated" -name "*.py" -printf "- %P\n" 2>/dev/null || echo "None found")

## Ground Truth Files
$(find "${PATCHES_DIR}/ground-truth" -name "*.py" -printf "- %P\n" 2>/dev/null || echo "None found")

## Next Steps
Run the comparison: \`python3 compare_tests.py ${PR_NUMBER}\`
SUMEOF

echo ""
echo "=== Collection Complete ==="
echo ""
echo "Results saved to: ${RESULTS_DIR}/"
echo "  generated/           - TestBot-generated test files"
echo "  comments/            - PR comments (TestBot analysis)"
echo "  logs/                - Workflow run logs"
echo "  testbot-diff.patch   - Full diff of TestBot's changes"
echo "  collection_summary.md - Summary"
echo ""
echo "Generated test files: ${GENERATED_TEST_COUNT}"
echo "Ground truth test files: ${GROUND_TRUTH_TEST_COUNT}"
echo ""
echo "Next: Run comparison with:"
echo "  python3 compare_tests.py ${PR_NUMBER}"
